package com.goblinskeep.UI;

/**
 * Enum representing the different options available in the game menu.
 */
public enum Options {
    /**
     * Option to resume the game.
     */
    RESUME ,
    /**
     * Option to quit the game.
     */
    QUIT,
    /**
     * Option to restart the game.
     */
    RESTART,
    /**
     * Option to go to the main menu.
     */
    MENU,
    /**
     * Option to view the instructions.
     */
    INSTRUCTIONS
}
